// ═══════════════════════════════════════════════════════════════
// ICON SIZE CONSTANTS
// ═══════════════════════════════════════════════════════════════

abstract final class AppIconSizes {
  // ============================= Basic Sizes =================================

  /// Extra small icons (12px) = Inline text icons, decorative elements
  static const double _xs = 12;

  /// Small icons (16px) = Compact UI, list items, chips
  static const double _sm = 16;

  /// Medium icons (20px) = Input fields, secondary actions
  static const double _md = 20;

  /// Regular icons (24px) = Default size, standard actions
  static const double _lg = 24;

  /// Large icons (32px) = Primary actions, prominent features
  static const double _xl = 32;

  /// Extra large icons (40px) = Feature highlights, empty states
  static const double _xxl = 40;

  /// 2X extra large icons (48px) = Hero sections, splash screens
  static const double _xxxl = 48;

  /// Huge icons (64px) = Large illustrations, onboarding
  static const double _huge = 64;

  // ============================= Semantic ====================================

  /// Icons in navigation bars and tabs
  static const double navigation = _lg;

  /// Icons in app bar actions
  static const double appBar = _lg;

  /// Icons in buttons (text, outlined)
  static const double button = _md;

  /// Icons in FAB
  static const double fab = _lg;

  /// Icons in list tiles (leading/trailing)
  static const double listTile = _lg;

  /// Icons in text fields (prefix/suffix)
  static const double textField = _md;

  /// Icons in chips and tags
  static const double chip = _sm;

  /// Icons in cards (header actions)
  static const double card = _md;

  /// Icons in dialogs (title area)
  static const double dialog = _xl;

  /// Icons in snackbars
  static const double snackBar = _md;

  /// Icons for avatars (small)
  static const double avatarSmall = _xl;

  /// Icons for avatars (medium)
  static const double avatarMedium = _xxl;

  /// Icons for avatars (large)
  static const double avatarLarge = _xxxl;

  /// Icons in empty states
  static const double emptyState = _xxxl;

  /// Icons in onboarding screens
  static const double onboarding = _huge;

  /// Icons in splash screens
  static const double splash = _huge;
}
